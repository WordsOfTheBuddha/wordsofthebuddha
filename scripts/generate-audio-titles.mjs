#!/usr/bin/env node
/**
 * Generate src/data/audioTitles.generated.ts — slug → title map for all
 * audio-bearing discourses. Read from `public/audio/<slug>.manifest.json`
 * (post-backfill manifests carry `title`); falls back to MDX frontmatter
 * for any manifest that hasn't been backfilled yet.
 *
 * Replaces the previous prerendered Astro endpoint at
 * `src/pages/audio-titles.json.ts`. Bundling the data into the JS payload
 * removes a runtime fetch and keeps the listen-mode boot path synchronous
 * for title lookups.
 *
 * Usage: node scripts/generate-audio-titles.mjs
 */
import fs from "node:fs";
import path from "node:path";
import { fileURLToPath } from "node:url";

const __dirname = path.dirname(fileURLToPath(import.meta.url));
const REPO_ROOT = path.join(__dirname, "..");
const AUDIO_DIR = path.join(REPO_ROOT, "public", "audio");
const CONTENT_DIR = path.join(REPO_ROOT, "src", "content", "en");
const OUT = path.join(REPO_ROOT, "src", "data", "audioTitles.generated.ts");
const OUT_DURATIONS = path.join(
	REPO_ROOT,
	"src",
	"data",
	"audioDurations.generated.ts",
);

/** Minimal frontmatter scan: only top-level scalar `key: value`. Mirrors
 * the Python helper `_parse_frontmatter` in scripts/generate_voice.py. */
function parseFrontmatterTitle(raw) {
	if (!raw.startsWith("---")) return null;
	const end = raw.indexOf("\n---", 3);
	if (end === -1) return null;
	const block = raw.slice(3, end);
	for (const line of block.split(/\r?\n/)) {
		const m = line.match(/^title\s*:\s*(.+?)\s*$/);
		if (!m) continue;
		let v = m[1];
		if ((v.startsWith('"') && v.endsWith('"')) || (v.startsWith("'") && v.endsWith("'"))) {
			v = v.slice(1, -1);
		}
		return v;
	}
	return null;
}

function findMdxTitle(slug) {
	const stack = [CONTENT_DIR];
	while (stack.length) {
		const dir = stack.pop();
		let entries;
		try {
			entries = fs.readdirSync(dir, { withFileTypes: true });
		} catch {
			continue;
		}
		for (const e of entries) {
			const p = path.join(dir, e.name);
			if (e.isDirectory()) stack.push(p);
			else if (e.isFile() && e.name === `${slug}.mdx`) {
				try {
					return parseFrontmatterTitle(fs.readFileSync(p, "utf8"));
				} catch {
					return null;
				}
			}
		}
	}
	return null;
}

function collectTitles() {
	if (!fs.existsSync(AUDIO_DIR)) return { titles: {}, durations: {} };
	const titles = {};
	const durations = {};
	for (const f of fs.readdirSync(AUDIO_DIR)) {
		if (!f.endsWith(".manifest.json")) continue;
		const slug = f.slice(0, -".manifest.json".length);
		try {
			const raw = fs.readFileSync(path.join(AUDIO_DIR, f), "utf8");
			const m = JSON.parse(raw);
			let title = typeof m.title === "string" && m.title ? m.title : null;
			if (!title) title = findMdxTitle(slug);
			if (title) titles[slug] = title;
			if (typeof m.duration === "number" && Number.isFinite(m.duration)) {
				// Round to whole seconds — sub-second precision isn't useful
				// for queue display and shrinks the bundled JSON noticeably.
				durations[slug] = Math.round(m.duration);
			}
		} catch {
			// Skip unreadable / unparseable manifests; better to omit than ship a stale title.
		}
	}
	return { titles, durations };
}

function writeOut({ titles, durations }) {
	const sortedT = Object.fromEntries(
		Object.entries(titles).sort(([a], [b]) => a.localeCompare(b)),
	);
	const sortedD = Object.fromEntries(
		Object.entries(durations).sort(([a], [b]) => a.localeCompare(b)),
	);
	const body =
		"// AUTO-GENERATED by scripts/generate-audio-titles.mjs — do not edit by hand.\n" +
		"// Re-run via `npm run prebuild` (chained) or directly with the script above.\n" +
		"export const audioTitles: Readonly<Record<string, string>> = " +
		JSON.stringify(sortedT, null, 2) +
		";\n";
	fs.mkdirSync(path.dirname(OUT), { recursive: true });
	fs.writeFileSync(OUT, body, "utf8");
	console.log(
		`Wrote ${path.relative(REPO_ROOT, OUT)} (${Object.keys(sortedT).length} entries)`,
	);

	const durationsBody =
		"// AUTO-GENERATED by scripts/generate-audio-titles.mjs — do not edit by hand.\n" +
		"// Per-discourse audio duration in **whole seconds** (rounded). Used by\n" +
		"// listen-mode UI for queue stats; absent slugs simply omit a stat.\n" +
		"export const audioDurations: Readonly<Record<string, number>> = " +
		JSON.stringify(sortedD, null, 2) +
		";\n";
	fs.writeFileSync(OUT_DURATIONS, durationsBody, "utf8");
	console.log(
		`Wrote ${path.relative(REPO_ROOT, OUT_DURATIONS)} (${Object.keys(sortedD).length} entries)`,
	);
}

writeOut(collectTitles());
